/* USER CODE BEGIN 1 */
uint8_t gUART2RxData = 0;
uint8_t gUART2RxBuf[8] = {0};
uint8_t gUART2RxIdx = 0;

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if(huart->Instance == USART2)
    {
        gUART2RxBuf[gUART2RxIdx++] = gUART2RxData;
        
        if(gUART2RxIdx == 1 && gUART2RxBuf[0] != 0xBB)
        {
            gUART2RxIdx = 0;
        }
        else if(gUART2RxIdx == 8)
        {
            uint8_t sum = 0;
            for(int i = 0; i < 7; i++) sum += gUART2RxBuf[i];
            
            if(sum == gUART2RxBuf[7])
            {
                gLeftSpeed = (gUART2RxBuf[1] << 8) | gUART2RxBuf[2];
                gRightSpeed = (gUART2RxBuf[3] << 8) | gUART2RxBuf[4];
                gCarStatus = gUART2RxBuf[5];
                gCarMode = gUART2RxBuf[6];
                
                // 同步物理按键状态，防止被覆盖 (0:遥控, 1:避障, 2:巡线, 3:规划)
                if (gCarMode <= 3) 
                {
                    gUserButtonCnt = gCarMode;
                }
            }
            gUART2RxIdx = 0;
        }
        HAL_UART_Receive_IT(&huart2, &gUART2RxData, 1);
    }
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart)
{
    if(huart->Instance == USART2)
    {
        __HAL_UART_CLEAR_FLAG(huart, UART_CLEAR_FEF | UART_CLEAR_NEF | UART_CLEAR_OREF);
        HAL_UART_Receive_IT(huart, &gUART2RxData, 1);
    }
}
